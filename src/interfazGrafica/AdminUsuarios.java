    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        for (int i = 0; i < jTable1.getRowCount(); i++) {

            if (IsSelected(i, 0, jTable1)) {
                int codigoEmpleado = Integer.parseInt(jTable1.getValueAt(i, 2).toString());
                Empleado e = new Empleado(codigoEmpleado, null, 0, null, null, null, null, null, null);

                Empleado actual = Controlador.ArbolEmpleadosTotales.find(Controlador.ArbolEmpleadosTotales.getRoot(), e).getKey();

                if ("habilitado".equals(actual.getEstado())) {
                    actual.setEstado("deshabilitado");
                } else {
                    actual.setEstado("habilitado");
                }

                System.out.println("empleados modificados");

                Controlador.ArbolEmpleadosTotales.find(Controlador.ArbolEmpleadosTotales.getRoot(), e).setKey(actual);

            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

