    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Paginador.vaciarPilas();
        Paginador.cargarPilas();

        if (rowCreated) {
            for (int i = M.getRowCount() - 1; i >= 0; i--) {
                M.removeRow(i);

            }
            rowCreated = false;

        }
        Paginador.counter = 0;
        int[] nextArray = nextPage(Paginador.pilaA, Paginador.pilaB);

        Controlador.ArbolEmpleadosTotales.printByRange(Controlador.ArbolEmpleadosTotales.getRoot(), nextArray[0], nextArray[1]);

        Object[] elements = Paginador.currentPage;
        Object[] o = null;

        for (int i = 0; i < elements.length; i++) {

            Empleado e = (Empleado) elements[i];

            M.addRow(o);
            M.setValueAt(e.getRol(), i, 1);
            M.setValueAt(e.getCodigoEmpleado(), i, 2);
            M.setValueAt(e.getLugarTrabajo(), i, 3);
            M.setValueAt(e.getEstado(), i, 4);

            M.setValueAt(e.getDocumento(), i, 5);
            M.setValueAt(e.getNombre(), i, 6);

            M.setValueAt(e.getApellido(), i, 7);
            M.setValueAt(e.getFechaNacimiento(), i, 8);
            M.setValueAt(e.getTelefono(), i, 9);
        }
        addCheckBox(0, jTable1); //repair
        rowCreated = true;
    }//GEN-LAST:event_jButton2ActionPerformed

